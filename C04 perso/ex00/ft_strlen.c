#include <unistd.h>
#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	*str = "abcdef";
	int size;

	size = ft_strlen(str);
	ft_strlen(str);
	printf("nombre de char: %d\n", size);
}
