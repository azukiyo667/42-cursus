#include <unistd.h>
#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	src[] = "abcdef";
	char	dest[60];

	ft_strcpy (dest, src);
	printf("source: %s\n", src);
	printf("destination: %s\n", dest);
}