#include <unistd.h>
#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{			
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	src[] = "abcdef";
	char	dest[60];

	ft_strncpy(dest, src, 2);
	printf("source: %s\n", src);
	printf("destination: %s\n", dest);
	return (0);
}
